#include "3-calc.h"

/**
 *op_add - check code
 *@a: the first number
 *@b: the second number
 *
 *Return:the sum
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 *op_sub - check code
 *@a: the first number
 *@b: the second number
 *
 *Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul - check code.
 *@a: the first number
 *@b: the second number
 *
 *Return: the multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 *op_div - check code.
 *@a: the first number
 *@b: the second number
 *
 *Return: the quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 *op_mod - check code.
 *@a: the first number
 *@b: the second number
 *
 *Return: the remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
