#include "3-calc.h"

/**
 *op_add - check code
 *@a:the first number
 *@b:the second number
 *
 *Return:The sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 *op_sub - check code
 *@a:The first number
 *@b:The second number
 *
 *Return: The Difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul - check code.
 *@a:The first number
 *@b: The second number
 *
 *Return: The multiplication of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 *op_div - check code.
 *@a: The first number.
 *@b: The second number.
 *
 *Return: The quotient of a and b.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 *op_mod - check code.
 *@a: The first number.
 *@b: The second number.
 *
 *Return: The remainder of the division of a by b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
